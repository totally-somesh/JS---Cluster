function Addition(n1,n2){
    
    var add = n1 + n2;
    return add;
}

function Subtraction(n1,n2){
    
    var sub = n1 - n2;
    return sub;
}

function Multiplication(n1,n2){

    var multi = n1 * n2;
    return multi

}

function Division(n1,n2){

    var div = n1 / n2;
    return div;
}

module.exports = {Add : Addition,
Sub : Subtraction,
Multi : Multiplication,
Div : Division}




